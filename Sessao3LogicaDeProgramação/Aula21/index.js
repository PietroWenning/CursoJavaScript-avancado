//Operadores de comparacao
/*
> maior que
>= maior ou igual
< menor que
<= menor ou igual
== igualdade (valor) - Nao recomendado
=== igualdade estrita (valor e tipo) - Recomendado
!= diferente (valor) - Nao recomendado
!== diferente estrito (valor e tipo) - Recomendado
*/ 
console.log(10 > 5); //true
console.log(10 >= 10);
console.log(10 < 5);
console.log(10 <= 10);
console.log(10 == '10'); //true
console.log(10 === '10'); //false
console.log(10 != '10'); //false
console.log(10 !== '10')//true
