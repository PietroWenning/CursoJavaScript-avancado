const num1 = 10;
const num2 = 5;
console.log(num1 + num2); // Adição
console.log(num1 - num2); // Subtração
console.log(num1 * num2); // Multiplicação
console.log(num1 / num2); // Divisão
console.log(num1 % num2); // Módulo (resto da divisão)
console.log(num1 ** num2); // Exponenciação (num1 elevado a num2)
